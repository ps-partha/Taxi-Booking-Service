CREATE TABLE customer_data (
    fullname varchar(255),
    email varchar(255),
    phone varchar(255),
    packageType varchar(255),
    passengers varchar(255),
    startDest varchar(255),
    endDest varchar(255),
    rideDate varchar(255),
    rideTime varchar(255),
    luggage varchar(255),
);

CREATE TABLE airport_transpor (
    Passenger_Name varchar(255),
    Passenger_Email varchar(255),
    Passenger_Phone varchar(255),
    Departure_Airport varchar(255),
    Arrival_Airport varchar(255),
    Arrival_Time varchar(255),
    Passenger_Address varchar(255)
);
